export async function getChatsByUserId({
  id,
  limit,
  startingAfter,
  endingBefore,
}: {
  id: string;
  limit: number;
  startingAfter: string | null;
  endingBefore: string | null;
}) {
  try {
    const extendedLimit = limit + 1;

    // Combined query that gets all chats for the user using either direct ownership or project ownership
    const query = (whereCondition?: SQL<any>) => {
      const baseQuery = db
        .select()
        .from(chat)
        .where(
          whereCondition
            ? and(
                whereCondition,
                // Chat is either directly owned by user or is in a project owned by user
                or(
                  eq(chat.userId, id),
                  // Join through projects
                  and(
                    // Chat must have a project reference
                    not(isNull(chat.projectId)),
                    // And the project must be owned by this user
                    exists(
                      db
                        .select({ id: project.id })
                        .from(project)
                        .where(
                          and(
                            eq(project.userId, id),
                            eq(project.id, chat.projectId),
                          ),
                        ),
                    ),
                  ),
                ),
              )
            : or(
                eq(chat.userId, id),
                // Join through projects
                and(
                  // Chat must have a project reference
                  not(isNull(chat.projectId)),
                  // And the project must be owned by this user
                  exists(
                    db
                      .select({ id: project.id })
                      .from(project)
                      .where(
                        and(
                          eq(project.userId, id),
                          eq(project.id, chat.projectId),
                        ),
                      ),
                  ),
                ),
              ),
        )
        .orderBy(desc(chat.createdAt))
        .limit(extendedLimit);
      return baseQuery;
    };

    let filteredChats: Array<Chat> = [];

    if (startingAfter) {
      const [selectedChat] = await db
        .select()
        .from(chat)
        .where(eq(chat.id, startingAfter))
        .limit(1);

      if (!selectedChat) {
        throw new ChatSDKError(
          'not_found:database',
          `Chat with id ${startingAfter} not found`,
        );
      }

      filteredChats = await query(gt(chat.createdAt, selectedChat.createdAt));
    } else if (endingBefore) {
      const [selectedChat] = await db
        .select()
        .from(chat)
        .where(eq(chat.id, endingBefore))
        .limit(1);

      if (!selectedChat) {
        throw new ChatSDKError(
          'not_found:database',
          `Chat with id ${endingBefore} not found`,
        );
      }

      filteredChats = await query(lt(chat.createdAt, selectedChat.createdAt));
    } else {
      filteredChats = await query();
    }

    const hasMore = filteredChats.length > limit;

    return {
      chats: hasMore ? filteredChats.slice(0, limit) : filteredChats,
      hasMore,
    };
  } catch (error) {
    console.error('Error in getChatsByUserId:', error);
    throw new ChatSDKError(
      'bad_request:database',
      'Failed to get chats by user id',
    );
  }
}

